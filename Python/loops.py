# Loop in a list
print("Loop in a list:")
for i in [0, 1, 2, 3, 4, 5]:
    print(i)

# Loop in a range
print("Loop in a range:")
for i in range(6):
    print(i)
    
# Loop list of strings
print("Loop list of strings:")
names = ["Sam", "Bucky", "Zemo", "Sharon"]
for name in names:
    print(name)
    
# Loop characters of a strings
print("Loop characters of a strings:")
name = "Harry"
for character in name:
    print(character)